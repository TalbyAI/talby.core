    internal static string GetHintName(INamedTypeSymbol rootSymbol)
    {
        var fullyQualifiedName = rootSymbol.ToDisplayString(
            SymbolDisplayFormat.FullyQualifiedFormat
        );
        var sanitizedName = new string(
            fullyQualifiedName
                .Select(static character => char.IsLetterOrDigit(character) ? character : '_')
                .ToArray()
        );

        return $"{sanitizedName}.g.cs";
    }

    internal static string Generate(DiscriminatedUnionRootModel rootModel)
    {
        var builder = new StringBuilder();
        var rootSymbol = rootModel.RootSymbol;

        builder.AppendLine("// <auto-generated/>");
        builder.AppendLine("#nullable enable");
        builder.AppendLine();

        if (!rootSymbol.ContainingNamespace.IsGlobalNamespace)
        {
            builder.Append("namespace ");
            builder.Append(rootSymbol.ContainingNamespace.ToDisplayString());
            builder.AppendLine(";");
            builder.AppendLine();
        }

        AppendSummary(
            builder,
            0,
            $"Provides generated discriminated-union members for {rootSymbol.Name}."
        );
        AppendIndentedLine(
            builder,
            0,
            $"{GetTypeDeclarationPrefix(rootSymbol)} {rootSymbol.Name}{GetTypeParameterList(rootSymbol)}"
        );
        AppendIndentedLine(builder, 0, "{");

        foreach (var caseModel in rootModel.ValidCases)
        {
            AppendIsProperty(builder, caseModel, 1);
        }

        if (!rootModel.LeafCases.IsDefaultOrEmpty)
        {
            builder.AppendLine();
            AppendMatchResultMethod(builder, rootSymbol, rootModel.LeafCases, 1);

            builder.AppendLine();
            AppendMatchActionMethod(builder, rootSymbol, rootModel.LeafCases, 1);

            foreach (var caseModel in rootModel.LeafCases)
            {
                builder.AppendLine();
                AppendLeafMatchResultMethod(builder, caseModel, 1);
                builder.AppendLine();
                AppendLeafMatchActionMethod(builder, caseModel, 1);
            }
        }

        AppendIndentedLine(builder, 0, "}");

        return builder.ToString();
    }

    private static void AppendIsProperty(
        StringBuilder builder,
        DiscriminatedUnionCaseModel caseModel,
        int indentLevel
    )
    {
        var caseName = caseModel.Symbol.Name;

        AppendSummary(builder, indentLevel, $"Gets whether this instance is a {caseName}.");
        AppendIndentedLine(
            builder,
            indentLevel,
            $"public bool Is{caseName} => this is {GetTypeReference(caseModel.Symbol)};"
        );
    }

    private static void AppendMatchResultMethod(
        StringBuilder builder,
        INamedTypeSymbol rootSymbol,
        ImmutableArray<DiscriminatedUnionCaseModel> leafCases,
        int indentLevel
    )
    {
        AppendSummary(
            builder,
            indentLevel,
            $"Matches this instance against the {GetCaseNameList(leafCases)} cases."
        );
        AppendIndentedLine(
            builder,
            indentLevel,
            "/// <typeparam name=\"TResult\">The result type.</typeparam>"
        );

        foreach (var caseModel in leafCases)
        {
            AppendIndentedLine(
                builder,
                indentLevel,
                $"/// <param name=\"match{caseModel.Symbol.Name}Func\">The function invoked for {caseModel.Symbol.Name}.</param>"
            );
        }

        AppendIndentedLine(builder, indentLevel, "/// <returns>The matched result.</returns>");
        AppendIndentedLine(
            builder,
            indentLevel,
            $"/// <exception cref=\"global::System.InvalidOperationException\">Thrown when the runtime type is not one of the generated {rootSymbol.Name} cases.</exception>"
        );
        AppendIndentedLine(builder, indentLevel, "public TResult Match<TResult>(");

        for (var index = 0; index < leafCases.Length; index++)
        {
            var caseModel = leafCases[index];
            var separator = index == leafCases.Length - 1 ? string.Empty : ",";

            AppendIndentedLine(
                builder,
                indentLevel + 1,
                $"global::System.Func<{GetTypeReference(caseModel.Symbol)}, TResult> match{caseModel.Symbol.Name}Func{separator}"
            );
        }

        AppendIndentedLine(builder, indentLevel, ")");
        AppendIndentedLine(builder, indentLevel, "{");
        AppendIndentedLine(builder, indentLevel + 1, "return this switch");
        AppendIndentedLine(builder, indentLevel + 1, "{");

        foreach (var caseModel in leafCases)
        {
            AppendIndentedLine(
                builder,
                indentLevel + 2,
                $"{GetTypeReference(caseModel.Symbol)} caseValue => match{caseModel.Symbol.Name}Func(caseValue),"
            );
        }

        AppendIndentedLine(
            builder,
            indentLevel + 2,
            $"_ => throw new global::System.InvalidOperationException(\"Unknown {rootSymbol.Name} type.\"),"
        );
        AppendIndentedLine(builder, indentLevel + 1, "};");
        AppendIndentedLine(builder, indentLevel, "}");
    }

    private static void AppendMatchActionMethod(
        StringBuilder builder,

    private static string GetCaseNameList(ImmutableArray<DiscriminatedUnionCaseModel> cases)
    {
        return string.Join(", ", cases.Select(static caseModel => caseModel.Symbol.Name));
    }

    private static string GetTypeDeclarationPrefix(INamedTypeSymbol symbol)
    {
        if (symbol.IsRecord)
        {
            return symbol.TypeKind == TypeKind.Struct ? "partial record struct" : "partial record";
        }

        return symbol.TypeKind == TypeKind.Struct ? "partial struct" : "partial class";
    }

    private static string GetTypeReference(INamedTypeSymbol symbol)
    {
        return symbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
    }

    private static string GetTypeParameterList(INamedTypeSymbol symbol)
    {
        if (symbol.TypeParameters.Length == 0)
        {
            return string.Empty;
        }

        return $"<{string.Join(", ", symbol.TypeParameters.Select(static typeParameter => typeParameter.Name))}>";
    }

    private static void AppendSummary(StringBuilder builder, int indentLevel, string summary)
    {
        AppendIndentedLine(builder, indentLevel, "/// <summary>");
        AppendIndentedLine(builder, indentLevel, $"/// {summary}");
        AppendIndentedLine(builder, indentLevel, "/// </summary>");
    }

    private static void AppendIndentedLine(StringBuilder builder, int indentLevel, string text)
    {
        builder.Append(new string(' ', indentLevel * 4));
        builder.AppendLine(text);
    }
